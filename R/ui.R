#' Build the Shiny UI for the Atlas Explorer
#'
#' @param metadata_choices Character vector of metadata column names.
#' @return A Shiny UI definition (\code{fluidPage}).
#' @keywords internal
atlas_ui <- function(metadata_choices) {

  tab_label <- function(icon_char, text) {
    shiny::HTML(paste0('<span class="tab-icon">', icon_char, "</span>", text))
  }

  shiny::fluidPage(
    shiny::tags$head(
      shiny::tags$style(shiny::HTML(app_css()))
    ),

    # ---- App header ----
    shiny::tags$div(class = "app-header",
      shiny::tags$span(class = "app-icon", "\U0001F9EC"),
      shiny::tags$div(
        shiny::tags$h1("Seurat Atlas Explorer"),
        shiny::tags$p(class = "app-subtitle",
                      "Single-cell RNA-seq analysis \u00b7 Interactive dashboard")
      )
    ),

    # ---- Tab wrapper ----
    shiny::tags$div(class = "nav-wrapper",
      shiny::tabsetPanel(id = "main_tabs",

        # ============================================================
        # TAB 0 — About
        # ============================================================
        shiny::tabPanel(tab_label("\u2139\uFE0F", "About"),
          shiny::tags$div(style = "max-width:1140px;margin:0 auto;padding:22px 12px 0;",

            shiny::tags$div(class = "info-hero",
              shiny::tags$span(class = "hero-badge",
                               "Bioinformatics \u00b7 scRNA-seq \u00b7 Single-cell Atlas"),
              shiny::tags$h2("Welcome to Seurat Atlas Explorer"),
              shiny::tags$p(
                "A modular, interactive dashboard for the in-depth exploration of single-cell ",
                "RNA-seq atlases \u2014 from UMAP visualisation and differential expression to ",
                "transcription factor regulon analysis and co-expression networks. ",
                "All computations run live on the Seurat object passed to ",
                shiny::tags$code("launch_explorer()"), "."
              )
            ),

            shiny::tags$div(class = "info-tip",
              shiny::tags$strong("How to use: "),
              "Select any tab from the navigation bar above. Choose your parameters in the ",
              "left-hand sidebar and press the action button. All results appear in the main ",
              "panel and can be downloaded as PNG or CSV."
            ),

            shiny::tags$div(class = "dataset-block",
              shiny::tags$h3("\U0001F4C2 About this dataset"),
              shiny::tags$p(
                "This atlas was generated by the ", shiny::tags$strong("Goriely Lab"),
                " and comprises single-cell transcriptomic profiles spanning multiple ",
                "cell populations and tissue compartments. Cells were sequenced using ",
                "10x Genomics Chromium technology, aligned with Cell Ranger, and processed ",
                "in Seurat with Harmony-based batch correction for cross-sample integration."
              ),
              shiny::tags$p(
                "Dimensionality reduction was performed via PCA followed by UMAP on the ",
                "Harmony-corrected embedding (", shiny::tags$code("umap.harmony"),
                "). Cell-type annotation was carried out at multiple resolution levels ",
                "(Population_level1\u20133). Transcription factor activity was inferred using a ",
                "GRN approach, and co-expression modules were identified with hdWGCNA."
              ),
              shiny::tags$div(class = "ds-meta",
                lapply(c("10x Genomics Chromium", "Harmony integration",
                         "Multi-level annotation", "TF regulon analysis",
                         "hdWGCNA modules", "EnrichR pathway analysis"),
                       function(t) shiny::tags$span(class = "ds-tag", t))
              )
            ),

            shiny::tags$div(class = "feature-grid",
              .feature_card("\U0001F5FA\uFE0F", "UMAP + Expression", list(
                "UMAP coloured by any metadata column",
                "FeaturePlot, VlnPlot, DotPlot, DoHeatmap",
                "Blend mode: co-expression of two genes",
                "Split violin / feature plot by any metadata",
                "Top marker genes per cluster (FindMarkers)",
                "Adjustable display size, font and DPI")),
              .feature_card("\U0001F52C", "DEGs + Volcano + Enrichment", list(
                "Subset cells by any metadata value",
                "FindMarkers between any two groups",
                "Interactive volcano with ggrepel labels",
                "p-value and log2FC threshold sliders",
                "Enrichment run separately via EnrichR",
                "Full geneset tables, UP and DOWN CSVs")),
              .feature_card("\U0001F578\uFE0F", "TF Interaction Network", list(
                "Interactive drag-and-zoom visNetwork graph",
                "5 layout algorithms to choose from",
                "Filter by Gain threshold and top N targets",
                "Reverse search: find TFs regulating a gene",
                "TF\u2013TF only interaction mode",
                "Full DB or filtered network CSV download")),
              .feature_card("\U0001F517", "Co-expression Modules", list(
                "hdWGCNA hub gene tables (ranked by kME)",
                "Module size overview table",
                "Gene \u2192 Module lookup by gene name",
                "Per-module pathway enrichment (EnrichR)",
                "Sortable, filterable, paginated tables")),
              .feature_card("\U0001F525", "TF Regulon Heatmap", list(
                "Dot heatmap: TF expression vs regulon activity",
                "Side-by-side positive and negative regulons",
                "Filter by module membership or manual TF list",
                "Per-TF EnrichR enrichment (pos & neg)",
                "PNG heatmap and data CSV download")),
              .feature_card("\u2699\uFE0F", "Universal plot controls", list(
                "Per-plot pixel size sliders for display",
                "Separate inch + DPI controls for download",
                "Font / label size slider on every plot",
                "All tables: sortable, filterable, paginated",
                "All results: PNG and CSV download buttons"))
            ),

            shiny::tags$div(class = "card",
              shiny::tags$div(class = "card-title", "\U0001F4E6 Required R packages"),
              shiny::tags$div(class = "tech-pills",
                lapply(c("Seurat","ggplot2","dplyr","tidyr","plotly","DT",
                         "enrichR","patchwork","ggrepel","visNetwork",
                         "scales","stringr","tibble","hdWGCNA"),
                       function(p) shiny::tags$span(class = "tech-pill", p))
              )
            ),

