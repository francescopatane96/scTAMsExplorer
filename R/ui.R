              shiny::h4("Gene lookup"),
              shiny::textInput("gene_lookup","Gene name:",""),
              shiny::actionButton("run_gene_lookup","Find module", icon=shiny::icon("search"), class="btn-primary"),
              shiny::h4("Module enrichment"),
              shiny::uiOutput("coexp_module_selector"),
              shiny::selectInput("coexp_enrich_db","EnrichR database:",
