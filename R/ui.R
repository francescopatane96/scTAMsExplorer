                            "DotPlot" = "dot", "Heatmap" = "heatmap")),
              shiny::conditionalPanel("input.plot_type == 'feature'",
                shiny::checkboxInput("blend_mode", "Blend two genes (co-expression)", FALSE)),
              shiny::conditionalPanel("input.plot_type == 'feature' || input.plot_type == 'vln'",
                shiny::checkboxInput("use_split", "Split by metadata", FALSE),
                shiny::conditionalPanel("input.use_split",
                  shiny::selectInput("split_by", "Split by:", choices = metadata_choices,
                    selected = if (length(metadata_choices) > 1) metadata_choices[2]
