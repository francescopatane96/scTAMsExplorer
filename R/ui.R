              shiny::h4("Plot controls"),
              plot_size_controls("coexp_enrich", default_w=720, default_h=380),
              shiny::hr(),
              shiny::downloadButton("download_coexp_enrich",     "Enrichment CSV"),
              shiny::downloadButton("download_coexp_enrich_plot","Barplot PNG")
            ),
            shiny::mainPanel(width = 9,
              shiny::fluidRow(
                shiny::column(5, shiny::tags$div(class="card",
                  shiny::tags$div(class="card-title","\U0001F4CF Module sizes"),
                  DT::DTOutput("module_size_table"))),
                shiny::column(7, shiny::tags$div(class="card",
