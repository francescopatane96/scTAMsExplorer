                  shiny::uiOutput("reg_enrich_neg_container")))
              )
            )
          )
        )
      ) # tabsetPanel
    )   # nav-wrapper
  )     # fluidPage
}

# ---- Internal helper: feature card for the About page ----
.feature_card <- function(icon, title, items) {
  shiny::tags$div(class = "feature-card",
    shiny::tags$div(class = "fc-icon", icon),
    shiny::tags$h3(title),
    shiny::tags$ul(lapply(items, function(i) shiny::tags$li(i)))
  )
}
