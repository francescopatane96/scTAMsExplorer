              shiny::h4("Expression"),
              shiny::textInput("feature",  "Gene / Feature:", value = "MS4A1"),
              shiny::textInput("feature2", "Gene 2 (blend \u00b7 heatmap \u00b7 dot):", value = "CD3D"),
              shiny::radioButtons("plot_type", "Plot type:",
