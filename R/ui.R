              shiny::tags$div(class = "footer-credit",
                shiny::tags$strong("Goriely Lab"), shiny::tags$br(),
                "MRC Weatherall Institute of Molecular Medicine \u00b7 University of Oxford",
                shiny::tags$br(),
                shiny::tags$span(style = "font-size:12px;color:#6b7f96;",
                  "Dataset and analysis pipeline developed by the Goriely Lab. ",
                  "This dashboard was built for internal exploratory use.")
              ),
              shiny::tags$span(class = "footer-badge", "Goriely Lab \u00b7 Oxford")
            ),
            shiny::tags$div(style = "height:30px;")
          )
        ),

        # ============================================================
        # TAB 1 — UMAP + Expression
        # ============================================================
        shiny::tabPanel(tab_label("\U0001F5FA\uFE0F", "UMAP"),
          shiny::sidebarLayout(
            shiny::sidebarPanel(width = 3,
              shiny::h4("UMAP"),
              shiny::selectInput("umap_groupby", "Group by:",
                choices  = metadata_choices,
                selected = if ("Population_level3" %in% metadata_choices)
