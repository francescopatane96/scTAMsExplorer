              shiny::h4("Marker genes"),
              shiny::sliderInput("n_markers", "Top markers:", min = 5, max = 100, value = 20),
              shiny::actionButton("markers", "Compute markers", icon = shiny::icon("dna"), class = "btn-primary"),
              shiny::h4("Plot controls"),
              plot_size_controls("umap", default_w = 720, default_h = 500),
              plot_size_controls("expr", default_w = 720, default_h = 500),
              shiny::hr(),
              shiny::h5("Downloads"),
              shiny::downloadButton("download_umap",      "UMAP PNG"),
              shiny::downloadButton("download_expr_plot", "Expression PNG"),
              shiny::downloadButton("download_markers",   "Markers CSV")
            ),
            shiny::mainPanel(width = 9,
              shiny::tags$div(class = "card",
                shiny::tags$div(class = "card-title", "\U0001F5FA\uFE0F UMAP"),
                shiny::uiOutput("umap_container")),
              shiny::tags$div(class = "card",
                shiny::tags$div(class = "card-title", "\U0001F4CA Expression plot"),
                shiny::uiOutput("expr_container")),
              shiny::tags$div(class = "card",
                shiny::tags$div(class = "card-title", "\U0001F3F7\uFE0F Marker genes"),
                DT::DTOutput("marker_table"))
            )
          )
        ),

        # ============================================================
        # TAB 2 — DEGs
        # ============================================================
        shiny::tabPanel(tab_label("\U0001F52C", "DEGs"),
          shiny::sidebarLayout(
            shiny::sidebarPanel(width = 3,
              shiny::h4("Subset & compare"),
              shiny::selectInput("deg_subset_meta", "Subset by:", choices = metadata_choices,
                selected = if ("Population_level3" %in% metadata_choices) "Population_level3" else metadata_choices[1]),
              shiny::uiOutput("deg_subset_value_selector"),
              shiny::hr(),
              shiny::selectInput("deg_compare_meta", "Compare groups from:", choices = metadata_choices,
                selected = if ("tissue" %in% metadata_choices) "tissue" else metadata_choices[1]),
              shiny::uiOutput("deg_group1_selector"),
              shiny::uiOutput("deg_group2_selector"),
              shiny::h4("Thresholds"),
              shiny::sliderInput("pval_cut",    "Adj p-value cutoff:", min = 0.0001, max = 0.1,  value = 0.05),
              shiny::sliderInput("lfc_cut",     "|log2FC| cutoff:",    min = 0,      max = 5,    value = 0, step = 0.1),
              shiny::sliderInput("n_label",     "Volcano labels:",     min = 0,      max = 30,   value = 10),
              shiny::sliderInput("n_deg_table", "Rows in DEG table:",  min = 10,     max = 500,  value = 100),
              shiny::actionButton("deg", "Run DEG analysis", icon = shiny::icon("calculator"), class = "btn-primary"),
              shiny::h4("Enrichment"),
              shiny::selectInput("enrich_db", "EnrichR database:",
                choices = c("GO_Biological_Process_2023","GO_Molecular_Function_2023",
                            "KEGG_2021_Human","Reactome_2022")),
              shiny::sliderInput("n_enrich", "Top terms:", min = 5, max = 30, value = 15),
              shiny::actionButton("run_enrich", "Run Enrichment", icon = shiny::icon("chart-bar"), class = "btn-success"),
              shiny::h4("Plot controls"),
              plot_size_controls("volcano", default_w = 720, default_h = 540),
              plot_size_controls("enrich",  default_w = 720, default_h = 380),
              shiny::hr(), shiny::h5("Downloads"),
              shiny::downloadButton("download_degs",            "DEGs CSV"),
              shiny::downloadButton("download_volcano",         "Volcano PNG"),
              shiny::downloadButton("download_enrich_up",       "UP enrichment PNG"),
              shiny::downloadButton("download_enrich_down",     "DOWN enrichment PNG"),
              shiny::downloadButton("download_enrich_up_csv",   "UP enrichment CSV"),
              shiny::downloadButton("download_enrich_down_csv", "DOWN enrichment CSV")
            ),
            shiny::mainPanel(width = 9,
              shiny::tags$div(class = "card",
                shiny::tags$div(class = "card-title", "\U0001F4CB DEG results"),
                DT::DTOutput("deg_table")),
              shiny::tags$div(class = "card",
                shiny::tags$div(class = "card-title", "\U0001F30B Volcano plot"),
                shiny::uiOutput("volcano_container")),
              shiny::fluidRow(
                shiny::column(6, shiny::tags$div(class = "card",
                  shiny::tags$div(class = "card-title", "\u2B06\uFE0F UP-regulated enrichment"),
                  shiny::plotOutput("enrich_up_plot"), shiny::hr(),
                  shiny::h5(style="font-size:13px;color:#1a2e45;margin-top:10px;","Geneset table"),
                  DT::DTOutput("enrich_up_table"))),
                shiny::column(6, shiny::tags$div(class = "card",
                  shiny::tags$div(class = "card-title", "\u2B07\uFE0F DOWN-regulated enrichment"),
                  shiny::plotOutput("enrich_down_plot"), shiny::hr(),
                  shiny::h5(style="font-size:13px;color:#1a2e45;margin-top:10px;","Geneset table"),
                  DT::DTOutput("enrich_down_table")))
              )
            )
          )
        ),

        # ============================================================
        # TAB 3 — TF Network
        # ============================================================
        shiny::tabPanel(tab_label("\U0001F578\uFE0F", "TF Network"),
          shiny::sidebarLayout(
            shiny::sidebarPanel(width = 3,
              shiny::h4("Source TF"),
              shiny::textInput("tf_center", "TF (source):", "MAFB"),
              shiny::h4("Reverse search"),
              shiny::textInput("tf_target_search", "Find TFs regulating gene:", ""),
              shiny::h4("Filters"),
              shiny::sliderInput("min_gain",      "Min Gain:",      min=0, max=1, value=0.05, step=0.01),
              shiny::sliderInput("n_top_targets", "Top N targets:", min=5, max=200, value=50),
              shiny::radioButtons("target_level", "Depth:", choices=c("1-step"=1,"2-step"=2)),
              shiny::checkboxInput("tf_tf_only", "Show only TF\u2013TF interactions", FALSE),
              shiny::h4("Layout"),
              shiny::selectInput("network_layout", "Algorithm:",
                choices = c("Fruchterman-Reingold"="fr","Kamada-Kawai"="kk",
                            "Large graph (DrL)"="drl","Circle"="circle","Nicely"="nicely")),
              shiny::actionButton("update_network", "Update network",
                                  icon = shiny::icon("rotate"), class = "btn-primary"),
              shiny::hr(), shiny::h5("Downloads"),
              shiny::downloadButton("download_network_full",     "Full interaction DB (CSV)"),
              shiny::downloadButton("download_network_filtered", "Filtered network (CSV)")
            ),
            shiny::mainPanel(width = 9,
              shiny::tags$div(class = "card",
                shiny::tags$div(class = "card-title", "\U0001F578\uFE0F TF Interaction Network",
                  shiny::tags$span(style="font-size:11px;color:#8099b4;font-weight:400;margin-left:8px;",
                    "\U0001F535 TF node \u00b7 \u26AB Target gene \u00b7 \U0001F534 Center/query \u00b7 Blue edge = positive \u00b7 Red edge = negative")),
                visNetwork::visNetworkOutput("network_plot", height = "680px"))
            )
          )
        ),

        # ============================================================
        # TAB 4 — Co-expression Modules
        # ============================================================
        shiny::tabPanel(tab_label("\U0001F517", "Modules"),
